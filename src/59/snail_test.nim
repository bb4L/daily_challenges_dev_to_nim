import unittest, snail

suite "test snail":
    test "1":
        check(snail(@[@[1, 2, 3], @[4, 5, 6], @[7, 8, 9]]) == @[1, 2, 3, 6, 9,
                8, 7, 4, 5])
    test "2":
        check(snail(@[@[1, 2, 3, 1], @[4, 5, 6, 4], @[7, 8, 9, 7], @[7, 8, 9,
                7]]) == @[1, 2, 3, 1, 4, 7, 7, 9, 8, 7, 7, 4, 5, 6, 9, 8])
